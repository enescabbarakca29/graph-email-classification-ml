AA = np.zeros((n, n), dtype=float)

for z in range(n):
    if degrees[z] > 1:
        weight = 1.0 / np.log(degrees[z])
        neighbors = np.where(A[z] > 0)[0]
        for i in neighbors:
            for j in neighbors:
                if i != j:
                    AA[i, j] += weight

print("AA matrix shape:", AA.shape)

