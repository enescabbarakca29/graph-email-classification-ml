def random_walk(graph, start, walk_length):
    walk = [start]

    for _ in range(walk_length - 1):
        neighbors = list(graph.neighbors(walk[-1]))

        if len(neighbors) == 0:
            break

        next_node = random.choice(neighbors)
        walk.append(next_node)

    return walk

